                <li>controller</li>
                <p align='justify'> Controller merupakan kumpulan intruksi aksi yang 
                    menghubungkan model dan view. </p>
                    <p align='justify'>Dari penjelasan MVC dapat disimpulkan bahwa controller Sebagai
                        penghubung view dan model. </p>
